package pkg05_string;

public class MainClass {
	public static void main(String[] args) {
		// 문자열 연산자
		String s1 = "hello";
		String s2 = " world";
		System.out.println(s1 + s2);
		
		int n1 = 1;
		int n2 = 2;
		
		System.out.println("" +  n1 + n2);
		System.out.println(n1 + "" + n2);
		System.out.println(n1 + n2 + ""); 
	}
}
